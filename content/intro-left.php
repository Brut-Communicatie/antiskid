
<h3>MORE EFFICIENT AND SAFE RUNWAY OPERATIONS</h3>
<p>Weather conditions are becoming more and more extreme, and competition is ever-increasing. Antiskid® provides an answer to tighter safety legislation and offers significant cost reductions.
</br></br>
Antiskid® is a rigid surface that is applied over runway asphalt. Predominately comprised of epoxy and sharp basalt, the surface layer is similar to coarse sandpaper. As a result, Antiskid® minimizes the rolling resistance and loss of traction during acceleration on wet runways. It also provides directional stability, especially in crosswinds. Both features substantially contribute to safety during landing and takeoff.
</br></br>
The high-friction properties of Antiskid® reduce runway occupation time,  increasing capacity and, consequently, profitability.
</p>

